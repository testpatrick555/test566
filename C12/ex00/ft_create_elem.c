#include "ft_list.h"
#include <stdlib.h>

t_list	*ft_create_elem(void *data)
{
	t_list	*new_elem;

	new_elem = (t_list *)malloc(1 * sizeof(t_list));
	new_elem->next = 0;
	new_elem->data = data;
	return (new_elem);
}
